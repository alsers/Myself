// Hello.java; 演示java的开发步骤
// 1. public class Hello表示Hello是一个类，是一个public的类
// 2. Hello {}：类开始类结束需要用括号包裹
// 3. public static void main(String[] args) 表示一个主方法，也就是程序入口
// 4. main() {} 表示方法的开始与结束
// 5. System.out.println("hello, world~"); 表示输出括号引号中元素到屏幕，分号";"表示语句的结束

public class Hello {
    // 编写一个main方法
    public static void main(String[] args) {
        System.out.println("hello, world~");
    }
}